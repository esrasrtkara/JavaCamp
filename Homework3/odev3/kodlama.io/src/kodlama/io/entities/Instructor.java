package kodlama.io.entities;

public class Instructor {
	
	private String name;

	public Instructor(String name) {
		super();
		this.name = name;
	}

	public Instructor() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
